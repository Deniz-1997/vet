{
    /**
     * ApiControllerDictionaryCallLang constructor.
     */
    public function __construct()
    {
        parent::__construct();

        $this->setModuleResources(DictionaryOrganizationsModule::setModel(ModelDictionaryOrganizations::query()))
            ->setRoles(['administrator', 'subadministrator']);
    }

    /**
     * Remove the specified resource from storage.
     *
     * @param Request $request
     * @param int $id
     * @return Response|array|object
     * @throws Exception
     */
    public function destroy(Request $request, int $id)
    {
        if (!static::checkAccess()) {
            return $this->sendError('Нет доступ на выполнение задачи.', Response::HTTP_FORBIDDEN);
        }

        $model = $this->getModule()->getModel()->find($id);

        if (is_null($model)) {
            return $this->sendError('Not found record');
        }


        $index = 0;
        $array_deletes = collect([$model->id]);
        $prev_id = $model->id;

        do {
            $modelChild = ModelDictionaryOrganizations::whereParentId($prev_id)->get();

            foreach ($modelChild as $item) {
                if ($array_deletes->search($item->id) === false) {
                    $array_deletes->push($item->id);
                }
            }

            $prev_id = $array_deletes->get($index);

            $index++;
        } while ($index < ($array_deletes->count() + 5) && !is_null($prev_id));

        foreach ($array_deletes as $array_delete) {
            ModelDictionaryOrganizations::find($array_delete)->delete();
        }

        return $this->sendResponse(['message' => 'Record success deleted'], $request);
    }
}
